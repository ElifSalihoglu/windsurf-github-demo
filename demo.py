def greet(name):
    """
    A simple greeting function to demonstrate the project.
    
    Args:
        name (str): The name to greet
    
    Returns:
        str: A greeting message
    """
    return f"Hello, {name}! Welcome to the Windsurf GitHub Demo."

def main():
    print(greet("Windsurf User"))

if __name__ == "__main__":
    main()
